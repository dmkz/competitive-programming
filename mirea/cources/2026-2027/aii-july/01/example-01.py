# Основные операции со списком list в Python3

# Создание списка фиксированной длины и заполнение по индексам
n = 5
a = [0] * n
for i in range(n):
    a[i] = (i + 1) * 10

# Получение размера, чтение и изменение элемента
sz = len(a)
x = a[2]
a[2] = x + 5

# Создание пустого списка и добавление элементов в конец
b = []
b.append(5)
b.append(8)
b.append(13)

# Удаление последнего элемента и вывод результата
b.pop()
print(f"size = {sz}")
print("a =", *a)
print("b =", *b)