# Пример: все двоичные строки длины n через маски.

# Считываем длину строки.
n = int(input())

for mask in range(1 << n):
	s = []
	for i in range(n):
		# Бит i определяет символ на позиции i.
		if (mask >> i) & 1:
			s.append('1')
		else:
			s.append('0')
	# Печатаем строку, закодированную текущей маской.
	print(''.join(s))