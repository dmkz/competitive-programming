# Пример: все двоичные строки длины n через рекурсию.
# На каждой позиции выбираем символ '0' или '1'.

n = int(input())
current = []


# Рекурсивно достраиваем двоичную строку слева направо.
def dfs(pos: int) -> None:
	if pos == n:
		# Построили полную строку.
		print(''.join(current))
		return

	# Ветка 1: ставим на позицию pos символ '0'.
	current.append('0')
	dfs(pos + 1)
	current.pop()

	# Ветка 2: ставим на позицию pos символ '1'.
	current.append('1')
	dfs(pos + 1)
	current.pop()


# Запускаем обход из пустого префикса.
dfs(0)