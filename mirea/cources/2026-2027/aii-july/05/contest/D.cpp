// Сначала отсортируем массив. Тогда внутри любого окна элементы идут по возрастанию,
// поэтому минимум на полуинтервале [i, j) равен a[i], а максимум после добавления нового
// элемента равен просто a[j]. Значит, правую границу можно двигать, пока выполняется
// неравенство a[j] - a[i] <= d. Если справа от a[i] внутри окна лежит cnt = j - i - 1
// элементов, то вместе с a[i] они дают C(cnt, 2) подходящих троек.
#include <bits/stdc++.h>
using namespace std;
void solve() {
    // читаем данные:
    int n, d;
    cin >> n >> d;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    // сортируем вектор:
    sort(a.begin(), a.end());
    // запускаем два указателя
    int j = 0;
    int64_t answ = 0;
    for (int i = 0; i < n; i++) {
        // Двигаем правую границу j, пока выполняется неравенство:
        while (j < n && a[j] - a[i] <= d)
            j++;
        // Считаем количество троек с выбранным a[i]:
        if (int cnt = j - i - 1; cnt > 0)
            answ += cnt * (cnt - 1LL) / 2;
    }
    cout << answ << '\n';
}
main() {
    int tt; cin >> tt;
    while (tt --> 0) solve();
}
