int numDivs(int n) {
    int answ = 1;
    for (int x = 2; x * x <= n; x++)
        if (int y = n / x; x * y == n) {
            int k = 1;
            n = y;
            while (n % x == 0) {
                n /= x;
                k++;
            }
            answ *= (k + 1);
        }
    if (n > 1)
        answ *= 2;
    return answ;
}
